namespace MyApp\Libs;

Class Api {
    /**
     * response
     *
     * @return void
     */
    public static function response($method, $code, $output) {
        $return = [];
        $return['code'] = $code;
        $return['response'] = $output;
        if($method == 'json') {
            $return = json_encode($return);
        }
        return $return;
    }
}